import "dotenv/config";
import mongoose from "mongoose";
import { connectDatabase } from "../config/database.config.js";
import RoleModel from "../models/roles-permission.model.js";
import { RolePermissions } from "../utils/role-permission.js";

const seedRoles = async () => {
  console.log("Seeding roles started...");

  try {
    await connectDatabase();

    const session = await mongoose.startSession();
    session.startTransaction();

    console.log("Clearing existing roles...");
    await RoleModel.deleteMany({}, { session });

    for (const roleName of Object.keys(RolePermissions)) {
      const permissions = RolePermissions[roleName];

      // Check if the role already exists
      const existingRole = await RoleModel.findOne({ name: roleName }).session(
        session
      );
      if (!existingRole) {
        const newRole = new RoleModel({
          name: roleName,
          permissions,
        });
        await newRole.save({ session });
        console.log(`Role ${roleName} added with permissions.`);
      } else {
        console.log(`Role ${roleName} already exists.`);
      }
    }

    await session.commitTransaction();
    console.log("Transaction committed.");

    session.endSession();
    console.log("Session ended.");

    console.log("Seeding completed successfully.");
  } catch (error) {
    console.error("Error during seeding:", error);
  }
};

seedRoles().catch((error) =>
  console.error("Error running seed script:", error)
);
